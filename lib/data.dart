ಕಾರ್ಗಿಲ್‌ ಹೀರೊ ಲೆಫ್ಟಿನೆಂಟ್‌ ಮನೋಜ್‌ಕುಮಾರ್‌ ಪಾಂಡೆ ಗೂರ್ಖಾ ರೈಫಲ್ಸ್‌ನ ಕಿರಿಯ ಅಧಿಕಾರಿ. ಯುದ್ಧ ತೀವ್ರ ಸ್ಥಿತಿಯಲ್ಲಿದ್ದಾಗ, ಮನೋಜ್‌ಕುಮಾರ್‌ ಪಾಂಡೆ ನೇತೃತ್ವದ ತುಕಡಿಗೆ ಬಟಾಲಿಕೆ ಸೆಕ್ಟರ್‌ನಿಂದ ಎದುರಾಳಿ ಸೈನಿಕರನ್ನು ಹೊರಹಾಕುವ ಜವಾಬ್ದಾರಿ ನೀಡಲಾಯಿತು. 1999 ಜುಲೈ 3ರಂದು ಮಹತ್ವದ ದಿನ. ಖಲುಬಾರ್‌ ಪರ್ವತ ತುದಿಯಲ್ಲಿದ್ದ ಶತ್ರು ಸೈನಿಕರನ್ನು ಹೊರ ದಬ್ಬುವ ಕ್ಷ ಣವದು. ಮಧ್ಯರಾತ್ರಿಯ ವೇಳೆಗೆ ಪಾಂಡೆ ನೇತೃತ್ವದ ತಂಡ ಅಂತಿಮ ಗುರಿಯತ್ತ ಸಾಗಿತ್ತು. ಆದರೆ, ಅಲ್ಲಲ್ಲಿ ಕುಳಿತಿದ್ದ ಶತ್ರುಗಳು ಪಾಂಡೆ ನೇತೃತ್ವದ ತುಕಡಿ ಮೇಲೆ ದಾಳಿ ನಡೆಸಿ, ಅವರು ಮುಂದೆ ಹೋಗದಂತೆ ತಡೆದಿದ್ದರು. ಹಾಗಾಗಿ, ಬೆಳಕು ಮೂಡು ವಷ್ಟರಲ್ಲಿ ಅವರನ್ನು ಹೊಡೆದೋಡಿಸಬೇಕಿತ್ತು. ಕಷ್ಟಸಾಧ್ಯ ಕೆಲಸವದು; ಸಾಹಸಿ ಪಾಂಡೆ ಎದೆಗುಂದಲಿಲ್ಲ. ಕಿರಿದಾದ ಭಾಗದ ಮೂಲಕ ಶತ್ರುಗಳ ಸಮೀಪವೇ ತಮ್ಮ ತಂಡವನ್ನು ತಂದು ನಿಲ್ಲಿಸಿದರು. ಆಗ ಗುಂಡಿನ ದಾಳಿ ಆರಂಭವಾಯಿತು. ಆದರೂ ಪಾಂಡೆ ಮುನ್ನುಗ್ಗುವುದನ್ನು ನಿಲ್ಲಿಸಲಿಲ್ಲ. ಶತ್ರುಗಳ ಬಂಕರ್‌ಗಳನ್ನು ನಾಶಪಡಿಸುವಲ್ಲಿ ಯಶಸ್ವಿಯಾದರು. ಆದರೆ, ಗುಂಡಿನ ದಾಳಿಯಿಂದ ಸಾಕಷ್ಟು ಗಾಯಗೊಂಡಿದ್ದ ಪಾಂಡೆ ಅವರು ಕುಸಿದು ಬಿದ್ದು, ವೀರಮರಣ ಅಪ್ಪಿದರು. ಅವರ ಸಾವು ವ್ಯರ್ಥವಾಗಲಿಲ್ಲ. ಭಾರತೀಯರಿಗೆ ಮುಖ್ಯವಾಗಿದ್ದ ಖಲುಬಾರ್‌ ಪರ್ವತ ಭಾರತದ ಮರುವಶವಾಗಿತ್ತು.



