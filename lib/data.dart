ಚಳಿಗಾಲದ ಋತುವಿನ ಸಂದರ್ಭದಲ್ಲಿ, ಕಾಶ್ಮೀರದ ಹಿಮಾಚ್ಛಾದಿತ ಪರ್ವತ ಪ್ರದೇಶಗಳಲ್ಲಿ ಮೈಕೊರೆಯುವ ಚಳಿಯ ಕಾರಣದಿಂದ,LOCಯ ಆಯಾ ಬದಿಗಳಲ್ಲಿರುವ ಕೆಲವು ಮುಂಚೂಣಿ ಶಿಬಿರಗಳನ್ನು ತ್ಯಜಿಸುವುದು ಮತ್ತು ಅತಿಕ್ರಮಣಕ್ಕೆ ದಾರಿಯಾಗಬಹುದಾದ ಪ್ರದೇಶಗಳ ಗಸ್ತನ್ನು ಕುಂಠಿತಗೊಳಿಸುವುದು ಭಾರತ ಮತ್ತು ಪಾಕಿಸ್ತಾನದ ಎರಡೂ ಸೇನೆಗಳ ಸಾಮಾನ್ಯ ವಾಡಿಕೆಯಾಗಿತ್ತು. ಚಳಿಯ ತೀವ್ರತೆ ಕಡಿಮೆಯಾದ ಬಳಿಕ, ಮುಂಚೂಣಿ ಪ್ರದೇಶಗಳನ್ನು ಮರುವಶಕ್ಕೆ ತೆಗೆದುಕೊಂಡು ಪಹರೆಯನ್ನು ಆರಂಭಿಸಲಾಗುತ್ತದೆ.



1999ರ ಫೆಬ್ರವರಿ ತಿಂಗಳಲ್ಲಿ, ಪಾಕಿಸ್ತಾನದ ಸೇನೆಯು ಕಾರ್ಗಿಲ್ ಪ್ರದೇಶದ LOCಯ ತನ್ನ ಬದಿಯಲ್ಲಿ ತ್ಯಜಿಸಿದ್ದ ಶಿಬಿರಗಳನ್ನು ಮರು ಆಕ್ರಮಿಸಿಕೊಳ್ಳಲು ಆರಂಭಿಸಿತು, ಇದರ ಜೊತೆಗೆ LOCಯ ಭಾರತದ ಬದಿಯಲ್ಲಿರುವ ಶಿಬಿರಗಳ ಮೇಲೆ ಆಕ್ರಮಣಕ್ಕೆ ಕೂಡ ತನ್ನ ಪಡೆಗಳನ್ನು ಕಳಿಸಿತು. ಉನ್ನತ ವಿಶೇಷ ಸೇವೆ ಗುಂಪಿನ ಪಡೆಗಳು ಮತ್ತು ನಾರ್ಥರ್ನ್ ಲೈಟ್ ಇನ್‌ಫೇಂಟ್ರಿದ ನಾಲ್ಕರಿಂದ ಏಳು ತುಕಡಿಗಳು(ಆ ಸಂದರ್ಭದಲ್ಲಿ ಪಾಕಿಸ್ತಾನ ಸೇನೆಯ ಖಾಯಂ ಭಾಗವಾಗಿರದ ಅರೆಸೇನೆ ಪಡೆ)ಭಾರತದ ನಿಯಂತ್ರಿತ ಪ್ರದೇಶದ ಆಯಕಟ್ಟಿನ ಸ್ಥಳಗಳಲ್ಲಿ ರಹಸ್ಯವಾಗಿ ಮತ್ತು ಬಹಿರಂಗವಾಗಿ ಶಿಬಿರಗಳನ್ನು ಸ್ಥಾಪಿಸಿದವು. ಕೆಲವು ವರದಿಗಳ ಪ್ರಕಾರ, ಈ ಪಾಕಿಸ್ತಾನಿ ಪಡೆಗಳಿಗೆ ಕಾಶ್ಮೀರದ ಬಂಡುಕೋರರು ಮತ್ತು ಅಫ್ಘಾನ್ ಬಾಡಿಗೆ ಹಂತಕರ ಬೆಂಬಲವಿತ್ತು.



ಪಾಕಿಸ್ತಾನದ ಆಕ್ರಮಣ



ಅತಿಕ್ರಮಣ ಮತ್ತು ಸೇನೆ ಸ್ಥಾಪನೆ

ಚಳಿಗಾಲದ ಋತುವಿನ ಸಂದರ್ಭದಲ್ಲಿ, ಕಾಶ್ಮೀರದ ಹಿಮಾಚ್ಛಾದಿತ ಪರ್ವತ ಪ್ರದೇಶಗಳಲ್ಲಿ ಮೈಕೊರೆಯುವ ಚಳಿಯ ಕಾರಣದಿಂದ,LOCಯ ಆಯಾ ಬದಿಗಳಲ್ಲಿರುವ ಕೆಲವು ಮುಂಚೂಣಿ ಶಿಬಿರಗಳನ್ನು ತ್ಯಜಿಸುವುದು ಮತ್ತು ಅತಿಕ್ರಮಣಕ್ಕೆ ದಾರಿಯಾಗಬಹುದಾದ ಪ್ರದೇಶಗಳ ಗಸ್ತನ್ನು ಕುಂಠಿತಗೊಳಿಸುವುದು ಭಾರತ ಮತ್ತು ಪಾಕಿಸ್ತಾನದ ಎರಡೂ ಸೇನೆಗಳ ಸಾಮಾನ್ಯ ವಾಡಿಕೆಯಾಗಿತ್ತು. ಚಳಿಯ ತೀವ್ರತೆ ಕಡಿಮೆಯಾದ ಬಳಿಕ, ಮುಂಚೂಣಿ ಪ್ರದೇಶಗಳನ್ನು ಮರುವಶಕ್ಕೆ ತೆಗೆದುಕೊಂಡು ಪಹರೆಯನ್ನು ಆರಂಭಿಸಲಾಗುತ್ತದೆ.



1999ರ ಫೆಬ್ರವರಿ ತಿಂಗಳಲ್ಲಿ, ಪಾಕಿಸ್ತಾನದ ಸೇನೆಯು ಕಾರ್ಗಿಲ್ ಪ್ರದೇಶದ LOCಯ ತನ್ನ ಬದಿಯಲ್ಲಿ ತ್ಯಜಿಸಿದ್ದ ಶಿಬಿರಗಳನ್ನು ಮರು ಆಕ್ರಮಿಸಿಕೊಳ್ಳಲು ಆರಂಭಿಸಿತು, ಇದರ ಜೊತೆಗೆ LOCಯ ಭಾರತದ ಬದಿಯಲ್ಲಿರುವ ಶಿಬಿರಗಳ ಮೇಲೆ ಆಕ್ರಮಣಕ್ಕೆ ಕೂಡ ತನ್ನ ಪಡೆಗಳನ್ನು ಕಳಿಸಿತು. ಉನ್ನತ ವಿಶೇಷ ಸೇವೆ ಗುಂಪಿನ ಪಡೆಗಳು ಮತ್ತು ನಾರ್ಥರ್ನ್ ಲೈಟ್ ಇನ್‌ಫೇಂಟ್ರಿದ ನಾಲ್ಕರಿಂದ ಏಳು ತುಕಡಿಗಳು(ಆ ಸಂದರ್ಭದಲ್ಲಿ ಪಾಕಿಸ್ತಾನ ಸೇನೆಯ ಖಾಯಂ ಭಾಗವಾಗಿರದ ಅರೆಸೇನೆ ಪಡೆ)ಭಾರತದ ನಿಯಂತ್ರಿತ ಪ್ರದೇಶದ ಆಯಕಟ್ಟಿನ ಸ್ಥಳಗಳಲ್ಲಿ ರಹಸ್ಯವಾಗಿ ಮತ್ತು ಬಹಿರಂಗವಾಗಿ ಶಿಬಿರಗಳನ್ನು ಸ್ಥಾಪಿಸಿದವು. ಕೆಲವು ವರದಿಗಳ ಪ್ರಕಾರ, ಈ ಪಾಕಿಸ್ತಾನಿ ಪಡೆಗಳಿಗೆ ಕಾಶ್ಮೀರದ ಬಂಡುಕೋರರು ಮತ್ತು ಅಫ್ಘಾನ್ ಬಾಡಿಗೆ ಹಂತಕರ ಬೆಂಬಲವಿತ್ತು.



ಕೆಳ ಮುಷೋಖ್ ಕಣಿವೆಯ ಶಿಖರಗಳು, ಡ್ರಾಸ್‌‍ನ ಮಾರ್ಪೋ ಲಾ ಪರ್ವತ ಶ್ರೇಣಿಗಳು, ಕಾರ್ಗಿಲ್ ಬಳಿಯ ಕಕ್ಸಾರ್‌, ಸಿಂಧು ನದಿಯ ಪೂರ್ವಕ್ಕಿರುವ ಬಟಾಲಿಕ್ ವಲಯ, LOC ಉತ್ತರಕ್ಕೆ ತಿರಗುವ ಚೋರ್‌ಬಾಟ್ಲಾ ವಲಯದ ಶಿಖರಗಳು, ಮತ್ತು ಸಿಯಾಚಿನ್ ಪ್ರದೇಶದ ದಕ್ಷಿಣಕ್ಕಿರುವ ಟರ್ಟೊಕ್ ವಲಯದಲ್ಲಿ ಪಾಕಿಸ್ತಾನಿ ಸೇನೆಯ ಅತಿಕ್ರಮಣ ನಡೆಯಿತು.





ಪಾಕಿಸ್ತಾನದ ಅತಿಕ್ರಮಣ ಪತ್ತೆ ಮತ್ತು ಭಾರತದ ಸೇನೆ ಸಜ್ಜು

ಈ ಅತಿಕ್ರಮಣಗಳನ್ನು ಆರಂಭದಲ್ಲೇ ಗುರುತಿಸಲು ಸಾಧ್ಯವಾಗದೇ ಇರುವುದಕ್ಕೆ ಅನೇಕ ಕಾರಣಗಳಿವೆ: ಪಾಕಿಸ್ತಾನದ ಪಡೆಗಳು ಅತಿಕ್ರಮಿಸಿದ ಕೆಲವು ಪ್ರದೇಶಗಳಿಗೆ ಭಾರತ ತನ್ನ ಗಸ್ತುಪಡೆಗಳನ್ನು ಕಳಿಸಿರಲಿಲ್ಲ ಮತ್ತು ಕೆಲವು ಪ್ರದೇಶಗಳಲ್ಲಿ ಪಾಕಿಸ್ತಾನ ಭಾರೀ ಫಿರಂಗಿ ಗುಂಡಿನ ದಾಳಿಗಳನ್ನು ನಡೆಸುವ ಮ‌ೂಲಕ ಅತಿಕ್ರಮಣಕಾರರಿಗೆ ರಕ್ಷಣೆ ಒದಗಿಸಿತು. ಆದರೆ ಬಟಾಲಿಕ್ ವಲಯದ ಸ್ಥಳೀಯ ಕುರುಬನೊಬ್ಬ ನೀಡಿದ ಸುಳಿವನ್ನು ಆಧರಿಸಿ, ಮೇ ಎರಡನೇ ವಾರದಲ್ಲಿ ಕ್ಯಾಪ್ಟನ್ ಸೌರಬ್ ಕಾಲಿಯ ನೇತೃತ್ವದ ಭಾರತದ ಗಸ್ತು ಪಡೆ ಹೊಂಚು ಹಾಕಿದ ಬಳಿಕ[೩೮] ಪಾಕಿಸ್ತಾನ ಪಡೆಗಳ ಅತಿಕ್ರಮಣ ಬೆಳಕಿಗೆ ಬಂದಿತ್ತು. ಆರಂಭದಲ್ಲಿ, ಅತಿಕ್ರಮಣದ ಸ್ವರೂಪದ ಬಗ್ಗೆ ತುಸು ಮಾಹಿತಿ ಮಾತ್ರ ಗೊತ್ತಿದ್ದ ಆ ಪ್ರದೇಶದ ಭಾರತೀಯ ಪಡೆಗಳು, ಅತಿಕ್ರಮಣಕಾರರು ಜಿಹಾದಿಗಳಾಗಿದ್ದು ಕೆಲವೇ ದಿನಗಳಲ್ಲಿ ಅವರನ್ನು ತೆರವು ಮಾಡಬಹುದೆಂದು ವಾದಿಸಿದ್ದರು. ಆದರೆ LOCಯ ಇನ್ನೂ ಕೆಲವು ಕಡೆಗಳಲ್ಲಿ ಅತಿಕ್ರಮಣವನ್ನು ಪತ್ತೆಹಚ್ಚಿದ್ದು ಮತ್ತು ಅತಿಕ್ರಮಣಕಾರರು ಯೋಜಿಸಿದ ಭಿನ್ನ ತಂತ್ರಗಳಿಂದಾಗಿ ಒಟ್ಟು ದಾಳಿಯ ಯೋಜನೆ ಅತೀ ದೊಡ್ಡ ಪ್ರಮಾಣದಲ್ಲಿರುವುದು ಭಾರತೀಯ ಸೇನೆಗೆ ಮನದಟ್ಟಾಯಿತು. ಈ ನುಸುಳುವಿಕೆಯಿಂದ ವಶಪಡಿಸಿಕೊಂಡ ಒಟ್ಟುಪ್ರದೇಶ 130 km² - 200 km² ನಡುವೆಯಿದೆಯೆಂಬ ಸಾಮಾನ್ಯ ಅಭಿಪ್ರಾಯವಿದೆ.[೩೨][೩೯] ಆದಾಗ್ಯೂ, ಭಾರತದ 500 ಚದರ ಮೈಲು (1,300 km²)ಪ್ರದೇಶವನ್ನು ಆಕ್ರಮಿಸಲಾಗಿತ್ತೆಂದು ಮುಷರಫ್ ಹೇಳಿಕೆ ನೀಡಿದ್ದಾರೆ.



ಭಾರತ ಸರ್ಕಾರ 2,00,000 ಭಾರತೀಯ ಪಡೆಗಳನ್ನು ಸಜ್ಜುಗೊಳಿಸಿ ಆಪರೇಷನ್ ವಿಜಯ್ ಮ‌ೂಲಕ ಸೂಕ್ತ ಉತ್ತರ ನೀಡಿತು. ಆದಾಗ್ಯೂ, ಭೂಪ್ರದೇಶದ ಸ್ವರೂಪದಿಂದಾಗಿ ಡಿವಿಷನ್ ಮತ್ತು ಕಾರ್ಪ್ಸ್ ಪಡೆಗಳ ಕಾರ್ಯಾಚರಣೆಗಳನ್ನು ಕೈಗೊಳ್ಳಲಾಗಲಿಲ್ಲ. ರೆಜಿಮೆಂಟ್ ಅಥವಾ ಪದಾತಿದಳದ ಮಟ್ಟದಲ್ಲೇ ಬಹುತೇಕ ಹೋರಾಟವನ್ನು ನಡೆಸಲಾಯಿತು. ವಸ್ತುಶಃ 20,000 ಮಂದಿ ಸೈನಿಕರಿದ್ದ ಭಾರತೀಯ ಸೇನೆಯ ಎರಡು ವಿಭಾಗಗಳು, ಇದರೊಂದಿಗೆ ಭಾರತದ ಅರೆಸೇನೆ ಪಡೆಯ ಅನೇಕ ಸಾವಿರ ಮಂದಿ ಮತ್ತು ವಾಯುಪಡೆಯನ್ನು ಯುದ್ಧ ವಲಯದಲ್ಲಿ ನಿಯೋಜಿಸಲಾಯಿತು. ಕಾರ್ಗಿಲ್-ಡ್ರಾಸ್ ವಲಯದಲ್ಲಿ ಸೇನಾ ಕಾರ್ಯಾಚರಣೆಯಲ್ಲಿ ಭಾಗಿಯಾದ ಭಾರತೀಯ ಸೈನಿಕರ ಒಟ್ಟು ಸಂಖ್ಯೆ 30,000ಕ್ಕೆ ಸಮೀಪದಲ್ಲಿತ್ತು. ಪಾಕಿಸ್ತಾನದ ಸೇನೆ ಶಸ್ತ್ರಗಳನ್ನು ಸರಬರಾಜು ಮಾಡಿದವರು ಸೇರಿದಂತೆ ಅತಿಕ್ರಮಣಕಾರರ ಸಂಖ್ಯೆ ಸುಮಾರು 5,000ವೆಂದು ಯುದ್ಧ ಅಂತಿಮ ಹಂತದಲ್ಲಿ ಅಂದಾಜು ಮಾಡಲಾಗಿತ್ತು.[೮][೩೨][೩೭] ಹೆಚ್ಚುವರಿ ಫಿರಂಗಿ ಬೆಂಬಲ ನೀಡಿದ ಪಾಕಿಸ್ತಾನ ಆಕ್ರಮಿತ ಕಾಶ್ಮೀರದ ಪಡೆಗಳು ಕೂಡ ಈ ಅಂಕಿಅಂಶದಲ್ಲಿ ಸೇರಿವೆ.



ಭಾರತದ ಭೂಸೇನೆಯ ಜಮಾವಣೆಗೆ ಬೆಂಬಲವಾಗಿ ಭಾರತೀಯ ವಾಯುಪಡೆ ಆಪರೇಷನ್ ಸೇಫ್ಡ್ ಸಾಗರ್ ಕಾರ್ಯಾಚರಣೆ ಆರಂಭಿಸಿತು, ಆದರೆ ಎತ್ತರದ ಪ್ರದೇಶ ಮತ್ತು ಹವಾಮಾನ ಪರಿಸ್ಥಿತಿಗಳಿಂದಾಗಿ ಯುದ್ಧದ ಸಂದರ್ಭದಲ್ಲಿ ಅದರ ಪರಿಣಾಮಕಾರತ್ವ ಸೀಮಿತಗೊಂಡಿತು, ಇದರಿಂದಾಗಿ ಬಾಂಬ್ ಲೋಡ್‌ಗಳ ಸಂಖ್ಯೆ ಸೀಮಿತಗೊಂಡವು, ಅಲ್ಲದೆ ಬಳಸಬಹುದಾದ ವಾಯುನೆಲೆಗಳ ಸಂಖ್ಯೆ ಕೂಡ ಸೀಮಿತಗೊಂಡಿತು.



ಪಾಕಿಸ್ತಾನ ಬಂದರುಗಳಿಗೆ(ಮುಖ್ಯವಾಗಿ ಕರಾಚಿ ಬಂದರಿಗೆ) ತಡೆ ವಿಧಿಸುವ ಪ್ರಯತ್ನದ ಮ‌ೂಲಕ ಸರಬರಾಜು ಮಾರ್ಗಗಳನ್ನು ಕಡಿತಗೊಳಿಸಿ ಭಾರತೀಯ ನೌಕಾಪಡೆ ಕೂಡ ಸ್ವತಃ ಯುದ್ಧ ಸಿದ್ಧತೆ ಮಾಡಿಕೊಂಡಿತು. ಬಳಿಕ, ಪೂರ್ಣ ಸ್ವರೂಪದ ಯುದ್ಧ ಭುಗಿಲೆದ್ದರೆ ಪಾಕಿಸ್ತಾನದಲ್ಲಿ ಕೇವಲ 6 ದಿನಗಳಿಗೆ ಸಾಕಾಗುವಷ್ಟು ಇಂಧನ ದಾಸ್ತಾನು ಉಳಿದುಕೊಂಡಿದೆಯೆಂದು ಆಗಿನ ಪ್ರಧಾನಮಂತ್ರಿ ನವಾಜ್ ಷರೀಫ್ ಬಹಿರಂಗಪಡಿಸಿದ್ದರು.



ಪಾಕಿಸ್ತಾನದ ನೆಲೆಗಳ ಮೇಲೆ ಭಾರತದ ದಾಳಿ

ಕಾಶ್ಮೀರದ ಭೂಪ್ರದೇಶ ಪರ್ವತಮಯವಾಗಿದ್ದು, ಅತೀ ಎತ್ತರದಲ್ಲಿದೆ; ಲೆಹ್‌ನಿಂದ ಶ್ರೀನಗರದವರೆಗಿನ ರಾಷ್ಟ್ರೀಯ ಹೆದ್ದಾರಿ 1D ಮುಂತಾದ ಅತ್ಯುತ್ತಮ ರಸ್ತೆಗಳಿಗೆ ಎರಡು ಪಥಗಳು ಮಾತ್ರ ಇವೆ. ಕಡಿದಾದ ಭೂಪ್ರದೇಶ ಮತ್ತು ಇಕ್ಕಟ್ಟಾದ ರಸ್ತೆಗಳು ಸೇನಾಪಡೆಯ ಸಂಚಾರವನ್ನು ನಿಧಾನಗೊಳಿಸಿತು ಮತ್ತು ಎತ್ತರ ಪ್ರದೇಶಗಳು ವಿಮಾನದಲ್ಲಿ ಸರಕುಗಳನ್ನು ಸಾಗಿಸುವ ಸಾಮರ್ಥ್ಯಕ್ಕೆ ಅಡ್ಡಿಯಾದ್ದರಿಂದ NH1 Dಯನ್ನು(ಪಾಕಿಸ್ತಾನದ ಗುಂಡಿನ ದಾಳಿಯ ಅಡಿಯಲ್ಲಿರುವ ಹೆದ್ದಾರಿಯ ವಾಸ್ತವ ವ್ಯಾಪ್ತಿ) ನಿಯಂತ್ರಣಕ್ಕೆ ತೆಗೆದುಕೊಳ್ಳುವುದು ಭಾರತದ ಆದ್ಯತೆಯಾಗಿತ್ತು. ತಮ್ಮ ವೀಕ್ಷಣಾ ನೆಲೆಗಳಿಂದ ಪರೋಕ್ಷವಾಗಿ NH 1D ಮೇಲೆ ಫಿರಂಗಿ ಗುಂಡಿನ ದಾಳಿ ನಡೆಸಿ ಭಾರತೀಯ ಪಡೆಗಳಲ್ಲಿ ಅಪಾರ ಸಾವುನೋವು ಉಂಟು ಮಾಡುವುದಕ್ಕಾಗಿ ಪಾಕಿಸ್ತಾನದ ಪಡೆಗಳು ಸ್ಪಷ್ಟವಾಗಿ ನೋಡಬಹುದಾದ ಮಾರ್ಗವಿತ್ತು.[೪೩] ಭಾರತದ ಸೇನೆಗೆ ಸೈನ್ಯ ಜಮಾವಣೆ ಮತ್ತು ಸರಬರಾಜಿಗೆ ಹೆದ್ದಾರಿಯೇ ಪ್ರಮುಖ ಮಾರ್ಗವಾದ್ದರಿಂದ ಗಂಭೀರ ಸಮಸ್ಯೆ ತಲೆದೋರಿತು.[೪೪] ಮುಖ್ಯ ಮಾರ್ಗದ ಮೇಲೆ ಪಾಕಿಸ್ತಾನ ಶೆಲ್ ದಾಳಿ ನಡೆಸುವುದರಿಂದ ಲೆಹ್ ಪ್ರತ್ಯೇಕವಾಗುವ ಆತಂಕವಿತ್ತು, ಆದರೂ ಹಿಮಾಚಲಪ್ರದೇಶದ ಮ‌ೂಲಕ ಲೆಹ್‌ಗೆ ಪರ್ಯಾಯ(ಉದ್ದದ)ರಸ್ತೆಯೊಂದು ಅಸ್ತಿತ್ವದಲ್ಲಿತ್ತು.



ಅತಿಕ್ರಮಣಕಾರರು ಸಣ್ಣ ಶಸ್ತ್ರಗಳು ಮತ್ತು ಗ್ರೆನೇಡ್ ಲಾಂಚರ್‌ಗಳನ್ನು ಹೊಂದಿದ್ದರಲ್ಲದೆ, ಮೋರ್ಟಾರ್‌ಗಳು, ಫಿರಂಗಿಗಳು ಮತ್ತು ವಿಮಾನ ನಿಗ್ರಹ ಬಂದೂಕುಗಳಿಂದ ಸಜ್ಜಿತರಾಗಿದ್ದರು. ಅನೇಕ ಶಿಬಿರಗಳಲ್ಲಿ ಭಾರೀ ನೆಲಬಾಂಬ್‌ಗಳನ್ನು ಹೂತಿಡಲಾಗಿತ್ತು,ICBL ವರದಿ ಪ್ರಕಾರ, ಯುದ್ಧಾನಂತರ ಭಾರತ 8,000ಕ್ಕೂ ಹೆಚ್ಚು ಸಿಬ್ಬಂದಿ ನಿಗ್ರಹ ನೆಲಬಾಂಬ್‌ಗಳನ್ನು ಪತ್ತೆ ಮಾಡಿದ್ದಾಗಿ ಹೇಳಿದೆ. ಅಮೆರಿಕ ಪೂರೈಸಿದ ಮಾನವರಹಿತ ವಿಮಾನಗಳು ಮತ್ತು AN/TPQ-36 ಫೈರ್‌ಫೈಂಡರ್ ರೆಡಾರ್‌ ಮ‌ೂಲಕ ಭಾರತದ ಪಡೆಗಳ ಮೇಲೆ ಪಾಕಿಸ್ತಾನ ಕಣ್ಗಾವಲು ನಡೆಸಿತ್ತು. NH 1Dಯನ್ನು ನೇರವಾಗಿ ನೋಡಬಹುದಾದ ಪರ್ವತಗಳನ್ನು ನಿಯಂತ್ರಣಕ್ಕೆ ತೆಗೆದುಕೊಳ್ಳುವುದು ಭಾರತದ ಆರಂಭಿಕ ದಾಳಿಗಳ ಗುರಿಯಾಗಿತ್ತು ಮತ್ತು ಕಾರ್ಗಿಲ್ ಪಟ್ಟಣದ ಹೆದ್ದಾರಿಯ ಚಾಚಿದ ಪ್ರದೇಶಗಳಿಗೆ ಅತೀ ಪ್ರಾಶಸ್ತ್ಯ ನೀಡಲಾಗಿತ್ತು. ನಿಯಂತ್ರಣ ರೇಖೆಯಲ್ಲಿರುವ ಬಹುತೇಕ ಶಿಬಿರಗಳು ಹೆದ್ದಾರಿಗೆ ಹೊಂದಿಕೊಂಡಿದ್ದರಿಂದ, ಹೆಚ್ಚುಕಡಿಮೆ ಪ್ರತಿಯೊಂದು ಅತಿಕ್ರಮಿತ ಶಿಬಿರಗಳ ಮರುವಶ ಪ್ರಾದೇಶಿಕ ಮತ್ತು ಹೆದ್ದಾರಿಯ ಭದ್ರತೆಗಳೆರಡನ್ನೂ ಹೆಚ್ಚಿಸಿದವು. ಯುದ್ಧದುದ್ದಕ್ಕೂ ಈ ಮಾರ್ಗದ ರಕ್ಷಣೆ ಮತ್ತು ಮುಂಚೂಣಿ ಶಿಬಿರಗಳ ಮರುವಶ ಕಾರ್ಯಾಚರಣೆ ಉದ್ದೇಶ ಗಳಾಗಿದ್ದವು.



NH 1Dಗೆ ಅತೀ ಸಮೀಪದಲ್ಲಿರುವ ಬೆಟ್ಟಗಳ ಮರುವಶ ಭಾರತದ ಸೇನಾಪಡೆಯ ಪ್ರಥಮ ಆದ್ಯತೆಯಾಗಿತ್ತು. ಇದರ ಫಲವಾಗಿ ಶ್ರೀನಗರ-ಲೆಹ್ ಮಾರ್ಗವನ್ನು ಆವರಿಸಿಕೊಂಡಿರುವ ಟೈಗರ್ ಹಿಲ್ ಮತ್ತು ಡ್ರಾಸ್‌ನ ಟೋಲೊಲಿಂಗ್ ಕಾಂಪ್ಲೆಕ್ಸ್ ಮೇಲೆ ಭಾರತೀಯ ಪಡೆಗಳು ಮೊದಲಿಗೆ ಗುರಿಯಿರಿಸಿದವು. ಇದರ ಹಿಂದೆಯೇ ತಕ್ಷಣವೇ ಸಿಯಾಚಿನ್ ನೀರ್ಗಲ್ಲು ಪ್ರದೇಶಕ್ಕೆ ಪ್ರವೇಶ ಕಲ್ಪಿಸುವ ಬಟಾಲಿಕ್-ಟರ್ಟೊಕ್ ಉಪ-ವಲಯ ಸೇನಾಪಡೆಗಳ ಗುರಿಯಾದವು. ಪಾಕಿಸ್ತಾನದ ರಕ್ಷಣಾತ್ಮಕ ಪಡೆಗಳಿಗೆ ಪಾಯಿಂಟ್ 4590 ಮತ್ತು ಪಾಯಿಂಟ್ 5353 ಮುಖ್ಯ ಆಯಕಟ್ಟಿನ ಸ್ಥಾನಗಳಲ್ಲಿರುವ ಕೆಲವು ಪರ್ವತಗಳಾಗಿತ್ತು. NH 1Dಯನ್ನು ಅತೀ ಸಮೀಪದಿಂದ ನೋಡಬಹುದಾದ ಸ್ಥಳವೆಂದರೆ ಪಾಯಿಂಟ್ 4590. ಪಾಯಿಂಟ್ 5353 ಡ್ರಾಸ್ ವಲಯದ ಅತ್ಯುನ್ನತ ಮೇಲ್ಮೈ ಲಕ್ಷಣದಿಂದ ಕೂಡಿದ್ದು, ಪಾಕಿಸ್ತಾನ ಪಡೆಗಳಿಗೆ NH 1D ಸರಾಗ ವೀಕ್ಷಣೆಗೆ ಅವಕಾಶ ಕಲ್ಪಿಸಿದ್ದವು.[೪೮] ಜೂನ್ 14ರಂದು ಪಾಯಿಂಟ್ 4590ನ್ನು ಭಾರತದ ಪಡೆಗಳು ಮರುವಶಕ್ಕೆ ತೆಗೆದುಕೊಂಡಿದ್ದು ಗಮನಾರ್ಹವೆನಿಸಿದರೂ,ಯುದ್ಧದ ವೇಳೆ ನಡೆದ ಏಕೈಕ ಚಕಮಕಿಯಲ್ಲಿ ಪಾಯಿಂಟ್ 4590ರಲ್ಲೇ ಭಾರತೀಯ ಸೇನೆ ಅತ್ಯಧಿಕ ಸಾವುನೋವು ಅನುಭವಿಸಿದ್ದು ಅಷ್ಟೇ ಸತ್ಯ. ಹೆದ್ದಾರಿಯ ಸಮೀಪದ ಬಹುತೇಕ ಶಿಬಿರಗಳನ್ನು ಜೂನ್ ಮಧ್ಯಾವಧಿಯಲ್ಲಿ ತೆರವುಗೊಳಿಸಲಾಯಿತಾದರೂ, ಡ್ರಾಸ್ ಬಳಿ ಹೆದ್ದಾರಿಯ ಕೆಲವು ಭಾಗಗಳು ಯುದ್ಧ ಅಂತ್ಯಗೊಳ್ಳುವ ತನಕ ವ್ಯಾಪಕ ಶೆಲ್ ದಾಳಿಗಳಿಗೆ ಸಾಕ್ಷಿಯಾದವು.

NH 1D ನೋಡಬಹುದಾದ ಶಿಖರಗಳ ಮೇಲೆ ಒಂದೊಮ್ಮೆ ಭಾರತ ಹಿಡಿತ ಮರುಸ್ಥಾಪಿಸಿದ ಬಳಿಕ, ಭಾರತದ ಸೇನೆಯು ಆಕ್ರಮಿತ ಸೇನೆಯನ್ನು ನಿಯಂತ್ರಣ ರೇಖೆಯ ಆಚೆಗೆ ಹಿಂದಕ್ಕೆ ದೂಡಲು ಕಾರ್ಯಾಚರಣೆ ಆರಂಭಿಸಿತು. ಇತರ ಪ್ರಹಾರಗಳೊಂದಿಗೆ, ಟೋಲೊಲಿಂಗ್ ಯುದ್ಧದ ನಂತರದ ಹೋರಾಟ ನಿಧಾನವಾಗಿ ಭಾರತದ ಪರವಾಗಿ ವಾಲಿತು. ಟೊಲೊಲಿಂಗ್‌ನಲ್ಲಿ ಕಾಶ್ಮೀರಕ್ಕೆ ಸೇರಿದ ಪಾಕಿಸ್ತಾನ ಪರ ಹೋರಾಟಗಾರರು ಪಾಕಿಸ್ತಾನಿ ಪಡೆಗಳಿಗೆ ನೆರವು ನೀಡಿದರು. ಯುದ್ಧಾಂತ್ಯದಲ್ಲಿ ಕೈವಶವಾದ ಟೈಗರ್ ಹಿಲ್(ಪಾಯಿಂಟ್ 5140) ಸೇರಿದಂತೆ ಅತಿಕ್ರಮಣಕಾರರ ಕೆಲವು ಶಿಬಿರಗಳು ತೀವ್ರ ಪ್ರತಿರೋಧ ಒಡ್ಡಿದವು. ಟೈಗರ್‌ಹಿಲ್ಸ್‌ನಲ್ಲಿ ಭಾರತದ ಪಡೆಗಳು ಭದ್ರವಾಗಿ ಬೇರೂರಿದ ಪಾಕಿಸ್ತಾನದ ಪಡೆಗಳನ್ನು ಕಂಡವು, ಮತ್ತು ಇಲ್ಲಿ ನಡೆದ ಹೋರಾಟದಲ್ಲಿ ಎರಡೂ ಪಡೆಗಳು ಸಾಕಷ್ಟು ಸಾವುನೋವು ಅನುಭವಿಸಿತು. ಟೈಗರ್ ಹಿಲ್‌ ಮೇಲೆ ನಡೆದ ಅಂತಿಮ ಪ್ರಹಾರದಲ್ಲಿ 10 ಪಾಕಿಸ್ತಾನಿ ಸೈನಿಕರು ಹತರಾಗಿ ಟೈಗರ್ ಹಿಲ್ ಭಾರತದ ಕೈವಶವಾಯಿತು, ಈ ಕಾರ್ಯಾಚರಣೆಯಲ್ಲಿ ಭಾರತ ಕೂಡ ತನ್ನ ಐವರು ಯೋಧರನ್ನು ಕಳೆದುಕೊಂಡಿತು. ಇದುವರೆಗೂ ಕೇಳಿರದ, ಕೆಲವೇ ಕೆಲವು ಪಾಯಿಂಟ್ ಸಂಖ್ಯೆಯ ವ್ಯತ್ಯಾಸಗಳಿರುವ ಬಹುತೇಕ ಹೆಸರಿಲ್ಲದ ಪರ್ವತಗಳಲ್ಲಿ ಕೆಲವು ಭೀಕರ ಕೈಮಿಸಲಾಯಿಸಿದ ಘಟನೆಗಳೂ ಸಂಭವಿಸಿವೆ.



ಕಾರ್ಯಾಚರಣೆ ಪೂರ್ಣಸ್ವರೂಪ ಪಡೆಯುತ್ತಿದ್ದಂತೆ ಕಾಣುವಂತಿದ್ದ ಶಿಬಿರಗಳಲ್ಲಿನ ಅತಿಕ್ರಮಣಕಾರರನ್ನು ತೆರವುಗೊಳಿಸಲು ಸುಮಾರು 250 ಫಿರಂಗಿ ಬಂದೂಕುಗಳನ್ನು ತರಲಾಯಿತು. ಇಂತಹ ಭೂಪ್ರದೇಶದಲ್ಲಿ ದಾಳಿ ನಡೆಸಲು ಸಹಕಾರಿಯಾಗುವ ಬೋಫೋರ್ಸ್ ಫೀಲ್ಡ್ ಹೋವಿಟ್ಜರ್ ಬಂದೂಕು(ಬೋಫೋರ್ಸ್ ಹಗರಣದ ಮ‌ೂಲಕ ಭಾರತದಲ್ಲಿ ಕುಖ್ಯಾತ)ಗಳನ್ನು ಭಾರತದ ಸೈನಿಕರು ಪೂರ್ಣ ಪ್ರಮಾಣದಲ್ಲಿ ಬಳಸಿಕೊಂಡರು, ಈ ಬಂದೂಕುಗಳು ಕಾರ್ಗಿಲ್ ಕಾರ್ಯಾಚರಣೆಯಲ್ಲಿ ಪ್ರಮುಖ ಪಾತ್ರವಹಿಸಿದವು. ಆದರೆ ಭೋಫೋರ್ಸ್ ಬಂದೂಕುಗಳ ನಿಯೋಜನೆಗೆ ಬೇಕಾಗುವ ಸ್ಥಳ ಮತ್ತು ಆಳ ಪ್ರದೇಶದ ಕೊರತೆಯಿಂದ ಬೇರೆ ಕಡೆಗಳಲ್ಲಿ ಅದರ ಯಶಸ್ಸು ಸೀಮಿತಗೊಂಡಿತು.



