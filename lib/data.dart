13 ಜಮ್ಮು ಮತ್ತು ಕಾಶ್ಮೀರ ರೈಫಲ್ಸ್‌ನ ಯೋಧ ಸಂಜಯ ಕುಮಾರ್‌ ಅವರ ಸಾಹಸಗಾಥೆಯೇ ರೋಮಾಂಚನ. 1999ರ ಜುಲೈ 4ರಂದು ಸಂಜಯಕುಮಾರ್‌ ಮತ್ತು ಅವರ ತಂಡಕ್ಕೆ ಮುಷ್ಕೋಹ ಕಣಿವೆಯ 4874 ಶಿಖರವನ್ನು ಕ್ಲಿಯರ್‌ಗೊಳಿಸುವ ಕೆಲಸ ಒಪ್ಪಿಸಲಾಯಿತು. ಸಂಜಯಕುಮಾರ್‌ ಅವರು ಸ್ಕೌಟ್‌ (ಮಾಹಿತಿ ಸಂಗ್ರಹ) ತಂಡವನ್ನು ಮುನ್ನಡೆಸುತ್ತಿದ್ದರು. ತಮ್ಮ ತಂಡದೊಂದಿಗೆ ಕಡಿದಾದ ಭಾಗವನ್ನು ಹತ್ತಿ ಶತ್ರು ಸೈನಿಕರತ್ತ ಧಾವಿಸಿದರು. ಆರಂಭದಲ್ಲಿ ಇವರಿಗೆ ಹಿನ್ನಡೆಯಾಯಿತು. ಆದರೆ, ಜುಲೈ 5 ಬೆಳಗಿನ ಜಾವದಲ್ಲಿ ಪರ್ವತದ ಮೇಲೆ ಅನುಕೂಲಕರ ಸ್ಥಿತಿಯಲ್ಲಿದ್ದ ಶತ್ರುಗಳ ಮೇಲೆ ದಾಳಿ ನಡೆಸಲು ನಿರ್ಧರಿಸಲಾಯಿತು. ಸಂಜಯಕುಮಾರ್‌ಗೆ ದಾಳಿಯ ನೇತೃತ್ವ ವಹಿಸಲಾಯಿತು. ಈ ಅವಕಾಶವನ್ನು ಬಳಸಿಕೊಂಡ ಅವರು, ಶತ್ರುಗಳ ಒಂದೊಂದೇ ಬಂಕರ್‌ ನಾಶ ಮಾಡುತ್ತಾ ಮುಂದೆ ಹೋದರು. ಮೊದಲನೇ ಬಂಕರ್‌ ನಾಶಪಡಿಸಿದಾಗಲೇ ಅವರ ಬಳಿಯಿದ್ದ ಗುಂಡುಗಳು ಖಾಲಿಯಾಗಿದ್ದವು. ವೈರಿಗಳ ಬಂದೂಕಿ ನಿಂದ 2 ಗುಂಡುಗಳು ಸಂಜಯಕುಮಾರ್‌ ತೊಡೆ ಹೊಕ್ಕಿದವು. ಆದರೂ ಎದೆಗುಂದದೆ, ದಾಳಿಕೋರರತ್ತ ನುಗ್ಗಿ ಅವರಿಂದಲೇ ಮಷಿನ್‌ ಗನ್‌ ಕಿತ್ತುಕೊಂಡು ಕಾದಾಡಿದರು. ಎರಡನೇ ಬಂಕರ್‌ ಕೂಡ ವಶಪಡಿಸಿಕೊಂಡರು. ಅಂತಿಮವಾಗಿ ಪಾಯಿಂಟ್‌ 4874 ಅನ್ನು ಭಾರತೀಯ ಸೇನೆ ತನ್ನದಾಗಿಸಿಕೊಂಡಿತು. ಸಂಜಯ ಕುಮಾರ್‌ ಅವರ ಸಾಹಸದ ಕತೆ ಎಲ್ಲೆಡೆ ಹರಡಿತು. 

